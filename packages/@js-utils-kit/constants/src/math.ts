/**
 * Ratio of a circle's circumference to its diameter.
 */
export const PI = Math.PI;

/**
 * Full circle constant (2 × PI).
 */
export const TAU = Math.PI * 2;

/**
 * Euler's number - the base of natural logarithms.
 */
export const E = Math.E;

/**
 * Square root of 2.
 */
export const SQRT_2 = Math.SQRT2;

/**
 * Square root of 1 / 2.
 */
export const SQRT_1_2 = Math.SQRT1_2;

/**
 * Natural logarithm of 2.
 *
 * Represents `ln(2)`.
 */
export const LN_2 = Math.LN2;

/**
 * Natural logarithm of 10.
 *
 * Represents `ln(10)`.
 */
export const LN_10 = Math.LN10;

/**
 * Base-2 logarithm of Euler's number.
 *
 * Represents `log₂(e)`.
 */
export const LOG2_E = Math.LOG2E;

/**
 * Base-10 logarithm of Euler's number.
 *
 * Represents `log₁₀(e)`.
 *
 * @default Math.LOG10E
 */
export const LOG10_E = Math.LOG10E;
